// 공유하기
function shareGunhap() {
  const title = document.querySelector('.sum-grade-desc')?.innerText || '사주 궁합 결과';
  const yA=document.getElementById('iYA').value, mA=document.getElementById('iMA').value, dA=document.getElementById('iDA').value;
  const yB=document.getElementById('iYB').value, mB=document.getElementById('iMB').value, dB=document.getElementById('iDB').value;
  const tuA=document.getElementById('timeUnknownA').checked;
  const tuB=document.getElementById('timeUnknownB').checked;
  const hA=tuA?'':document.getElementById('iHA').value;
  const hB=tuB?'':document.getElementById('iHB').value;
  const mnA=tuA?'':document.getElementById('iMinA').value;
  const mnB=tuB?'':document.getElementById('iMinB').value;

  const params = new URLSearchParams({
    yA,mA,dA, gA:genderA, tuA:tuA?'1':'0',
    yB,mB,dB, gB:genderB, tuB:tuB?'1':'0',
  });
  if (!tuA) { params.set('hA',hA); params.set('mnA',mnA); }
  if (!tuB) { params.set('hB',hB); params.set('mnB',mnB); }
  const url = `${location.origin}${location.pathname}?${params.toString()}`;

  if (navigator.share) {
    navigator.share({ title:'사주 궁합 — 길을 찾는 발걸음', text:title, url }).catch(()=>{});
  } else {
    navigator.clipboard?.writeText(url).then(() => {
      alert('궁합 결과 링크가 복사되었습니다!\n공유받은 분이 링크를 열면 결과가 바로 보입니다.');
    }).catch(() => { prompt('아래 링크를 복사하세요:', url); });
  }
}

