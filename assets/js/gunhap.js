function calcSaju(y, m, d, h, mn) {
  let cy = y, cm = m, cd = d;
  let nightZi = false;
  if (h === 23 && mn >= 30) {
    nightZi = true;
    const next = new Date(y, m-1, d+1);
    cy = next.getFullYear(); cm = next.getMonth()+1; cd = next.getDate();
  }
  return {
    year:  getYearGanzhi(cy, cm, cd, h, mn),
    month: getMonthGanzhi(cy, cm, cd, h, mn),
    day:   getDayGanzhi(cy, cm, cd),
    time:  (h >= 0) ? getTimeGanzhi(h, mn, getDayGanzhi(cy,cm,cd).ganIdx) : null,
    nightZi,
  };
}

