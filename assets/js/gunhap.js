// 천간 관계 분석
function analyzeCheongan(g1, g2) {
  const result = [];
  const a = Math.min(g1, g2), b = Math.max(g1, g2);
  CHEONGAN_HAP.forEach((pair, i) => {
    if (pair[0]===a && pair[1]===b) result.push({ type:'합', name:CHEONGAN_HAP_NAME[i], score:+20 });
  });
  CHEONGAN_CHUNG.forEach((pair, i) => {
    if (pair[0]===a && pair[1]===b) result.push({ type:'충', name:CHEONGAN_CHUNG_NAME[i], score:-15 });
  });
  if (GAN_OH[g1] === GAN_OH[g2]) result.push({ type:'비화', name:`${GAN_KOR[g1]}${GAN_KOR[g2]} 비화`, score:+5 });
  return result;
}

