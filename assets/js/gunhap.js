function analyzeOheng(oh1, oh2) {
  if (oh1 === oh2) return { rel:'비화', desc:'같은 오행으로 서로 이해하고 공감합니다.', score:+10 };
  if (OH_GENERATE[oh1]===oh2) return { rel:'상생', desc:`${oh1}이 ${oh2}을 생해줍니다. 한쪽이 다른 쪽을 도와주는 관계예요.`, score:+20 };
  if (OH_GENERATE[oh2]===oh1) return { rel:'상생', desc:`${oh2}이 ${oh1}을 생해줍니다. 한쪽이 다른 쪽을 도와주는 관계예요.`, score:+20 };
  if (OH_CONTROL[oh1]===oh2) return { rel:'상극', desc:`${oh1}이 ${oh2}을 극합니다. 자칫 한쪽이 주도권을 쥐려 할 수 있어요.`, score:-10 };
  if (OH_CONTROL[oh2]===oh1) return { rel:'상극', desc:`${oh2}이 ${oh1}을 극합니다. 자칫 한쪽이 주도권을 쥐려 할 수 있어요.`, score:-10 };
  return { rel:'중립', desc:'특별한 상생·상극 관계가 없습니다.', score:0 };
}

