// URL 파라미터로 자동 실행
function parseURLParams() {
  const p = new URLSearchParams(location.search);
  if (!p.get('yA') || !p.get('yB')) return;

  const set = (id, val) => { if (val) document.getElementById(id).value = val; };
  set('iYA', p.get('yA')); set('iMA', p.get('mA')); set('iDA', p.get('dA'));
  set('iYB', p.get('yB')); set('iMB', p.get('mB')); set('iDB', p.get('dB'));

  if (p.get('gA') === 'F') setGA('F');
  if (p.get('gB') === 'M') setGB('M');

  if (p.get('tuA') === '1') {
    document.getElementById('timeUnknownA').checked = true;
    toggleTimeA(document.getElementById('timeUnknownA'));
  } else { set('iHA', p.get('hA')); set('iMinA', p.get('mnA')); }

  if (p.get('tuB') === '1') {
    document.getElementById('timeUnknownB').checked = true;
    toggleTimeB(document.getElementById('timeUnknownB'));
  } else { set('iHB', p.get('hB')); set('iMinB', p.get('mnB')); }

  setTimeout(run, 100);
}

