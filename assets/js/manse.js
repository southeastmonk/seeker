// 절기 데이터에서 특정 연도의 특정 절기 인덱스 Date 객체 반환
// idx: 0=소한, 1=입춘, 2=경칩, 3=청명, 4=입하, 5=망종, 6=소서, 7=입추, 8=백로, 9=한로, 10=입동, 11=대설
function getJeolDate(year, idx) {
  const d = JEOL[year];
  if (!d) return null;
  const [m, day, h, min] = d[idx];
  return new Date(year, m - 1, day, h, min, 0);
}

// 일주 계산: 양력 기준 갑자일 기준점 사용
// 1900-01-01 = 甲戌일 (ganIdx=0 甲, jiIdx=10 戌) → 60갑자 index = 0*12... 실제로 산출
// 기준: 1900년 1월 1일 = 甲戌 (갑자일 아님) → 실제 계산
// 정확한 기준: Unix Epoch 기준 계산
function getDayGanzhi(year, month, day) {
  // 기준일: 1900년 1월 1일 = 갑술일
  // 갑술: gan=0(甲), ji=10(戌) → 60갑자 번호 = (0 + 10*6) % 60 ...
  // 쉽게: Julian Day 기반으로 60갑자 순환
  const jd = dateToJD(year, month, day);
  // JD 2415021 = 1900-01-01 = 甲戌일 (번호=10: 甲0*6+10=10번)
  // 60갑자 기준: 甲子=0, 乙丑=1, ..., 甲戌=10
  const BASE_JD = 2415021; // 1900-01-01
  const BASE_IDX = 10; // 甲戌=10
  let diff = jd - BASE_JD;
  let idx60 = ((diff % 60) + 60 + BASE_IDX) % 60;
  return { ganIdx: idx60 % 10, jiIdx: idx60 % 12 };
}

function dateToJD(y, m, d) {
  // 그레고리력 → 율리우스적일
  const a = Math.floor((14 - m) / 12);
  const yr = y + 4800 - a;
  const mn = m + 12 * a - 3;
  return d + Math.floor((153 * mn + 2) / 5) + 365 * yr +
    Math.floor(yr / 4) - Math.floor(yr / 100) + Math.floor(yr / 400) - 32045;
}

// 년주 계산: 입춘 기준
function getYearGanzhi(year, month, day, hour, min) {
  // 실제 입춘 시각 체크
  let y = year;
  const chunDate = getJeolDate(year, 1); // 입춘 = index 1
  const birthDate = new Date(year, month - 1, day, hour, min);
  if (chunDate && birthDate < chunDate) y = year - 1;
  // 기준: 1984년 = 甲子년 (idx=0)
  const base = 1984;
  let diff = y - base;
  let idx60 = ((diff % 60) + 60) % 60;
  return { ganIdx: idx60 % 10, jiIdx: idx60 % 12 };
}

