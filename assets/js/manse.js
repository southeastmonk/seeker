function getGongmang(dayGanIdx, dayJiIdx) {
  // 60갑자에서 일주 위치: 甲子=0, 乙丑=1, ..., 癸亥=59
  // 간지 번호: ganIdx와 jiIdx로 0~59 계산
  // 같은 천간이 6번 나오므로: idx = ganIdx + (jiIdx - ganIdx + 60) % 12 * 5 ... 아니면
  // 가장 간단: for loop으로 직접 탐색
  let idx60 = -1;
  for (let i = 0; i < 60; i++) {
    if (i % 10 === dayGanIdx && i % 12 === dayJiIdx) { idx60 = i; break; }
  }
  if (idx60 < 0) return [10, 11]; // fallback
  const junIdx = Math.floor(idx60 / 10); // 0~5
  return GONGMANG_MAP[junIdx];
}

