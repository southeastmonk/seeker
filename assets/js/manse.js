function calcDaewoon(by, bm, bd, bh, bmin, gender) {
  const yearGZ = getYearGanzhi(by, bm, bd, bh, bmin);
  const yinYang = yearGZ.ganIdx % 2 === 0 ? '양' : '음';
  const isForward = (yinYang === '양' && gender === 'M') || (yinYang === '음' && gender === 'F');
  const birthDate = new Date(by, bm - 1, bd, bh, bmin);
  let jeolDate = null;

  if (isForward) {
    outer: for (let y = by; y <= by + 1; y++) {
      if (!JEOL[y]) continue;
      for (let i = 0; i < 12; i++) {
        const jd = getJeolDate(y, i);
        if (jd && jd > birthDate) { jeolDate = jd; break outer; }
      }
    }
  } else {
    outer: for (let y = by + 1; y >= by - 1; y--) {
      if (!JEOL[y]) continue;
      for (let i = 11; i >= 0; i--) {
        const jd = getJeolDate(y, i);
        if (jd && jd < birthDate) { jeolDate = jd; break outer; }
      }
    }
  }

  const days = jeolDate ? Math.abs(jeolDate - birthDate) / 86400000 : 9;
  return { startAge: Math.round(days / 3), isForward };
}

function getDaewoonList(by, bm, bd, bh, bmin, gender) {
  const monthGZ = getMonthGanzhi(by, bm, bd, bh, bmin);
  const { startAge, isForward } = calcDaewoon(by, bm, bd, bh, bmin, gender);
  const list = [];
  for (let i = 0; i < 10; i++) {
    const ganIdx = isForward ? (monthGZ.ganIdx + i + 1) % 10 : ((monthGZ.ganIdx - i - 1 + 100) % 10);
    const jiIdx  = isForward ? (monthGZ.jiIdx  + i + 1) % 12 : ((monthGZ.jiIdx  - i - 1 + 120) % 12);
    list.push({ ganIdx, jiIdx, age: startAge + i * 10, endAge: startAge + i * 10 + 9 });
  }
  return { list, startAge };
}

