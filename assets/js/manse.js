// 공유
function share(url) {
  const title = document.querySelector('.sum-title')?.innerText || '사주 결과';
  const birth = document.querySelector('.sum-birth')?.innerText || '';
  if (navigator.share) {
    navigator.share({ title: '사주 만세력 — 길을 찾는 발걸음', text: `${birth}\n${title}`, url }).catch(() => {});
  } else {
    navigator.clipboard?.writeText(url).then(() => {
      alert('결과 링크가 복사되었습니다!\n공유받은 분이 링크를 열면 결과가 바로 보입니다.');
    }).catch(() => { prompt('아래 링크를 복사하세요:', url); });
  }
}

// 인쇄/PDF
function printResult() {
  // 모든 탭 패널 임시 표시
  const panels = ['panel-saju', 'panel-oheng', 'panel-daewoon'];
  panels.forEach(id => {
    const el = document.getElementById(id);
    if (el) el.classList.remove('hidden');
  });
  window.print();
  // 인쇄 후 원래 탭으로 복귀
  setTimeout(() => {
    panels.forEach(id => {
      const el = document.getElementById(id);
      if (el && id !== 'panel-' + activeTab) el.classList.add('hidden');
    });
  }, 500);
}

