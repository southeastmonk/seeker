function parseURLParams() {
  const p = new URLSearchParams(window.location.search);
  const y = p.get('y'), m = p.get('m'), d = p.get('d');
  const h = p.get('h'), min = p.get('min'), g = p.get('g'), tu = p.get('tu');
  if (!y || !m || !d) return;

  document.getElementById('iY').value = y;
  document.getElementById('iM').value = m;
  document.getElementById('iD').value = d;

  if (tu === '1') {
    document.getElementById('timeUnknown').checked = true;
    toggleTime(document.getElementById('timeUnknown'));
  } else if (h !== null) {
    document.getElementById('iH').value = h;
    document.getElementById('iMin').value = min || '0';
  }
  if (g === 'F') setG('F');
  setTimeout(run, 100);
}

function buildShareURL(y, m, d, h, mn, g, tu) {
  const base = window.location.origin + window.location.pathname;
  const params = new URLSearchParams({ y, m, d });
  if (tu) { params.set('tu', '1'); }
  else     { params.set('h', h); params.set('min', mn); }
  params.set('g', g);
  return `${base}?${params.toString()}`;
}

